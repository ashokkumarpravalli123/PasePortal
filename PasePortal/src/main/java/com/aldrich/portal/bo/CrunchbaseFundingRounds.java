package com.aldrich.portal.bo;

public class CrunchbaseFundingRounds {
	
	private String fundingType="";
	private String fundingSeries="";
	private String announcedOn="";
	private String moneyRaisedUSD="";
	private String noOfInvestors="";
	private String investorLinks="";
	private String investorNames="";
	private String investorType="";
	
	
	public String getFundingType() {
		return fundingType;
	}
	public void setFundingType(String fundingType) {
		this.fundingType = fundingType;
	}
	public String getFundingSeries() {
		return fundingSeries;
	}
	public void setFundingSeries(String fundingSeries) {
		this.fundingSeries = fundingSeries;
	}
	public String getAnnouncedOn() {
		return announcedOn;
	}
	public void setAnnouncedOn(String announcedOn) {
		this.announcedOn = announcedOn;
	}
	public String getMoneyRaisedUSD() {
		return moneyRaisedUSD;
	}
	public void setMoneyRaisedUSD(String moneyRaisedUSD) {
		this.moneyRaisedUSD = moneyRaisedUSD;
	}
	public String getNoOfInvestors() {
		return noOfInvestors;
	}
	public void setNoOfInvestors(String noOfInvestors) {
		this.noOfInvestors = noOfInvestors;
	}
	public String getInvestorLinks() {
		return investorLinks;
	}
	public void setInvestorLinks(String investorLinks) {
		this.investorLinks = investorLinks;
	}
	public String getInvestorNames() {
		return investorNames;
	}
	public void setInvestorNames(String investorNames) {
		this.investorNames = investorNames;
	}
	public String getInvestorType() {
		return investorType;
	}
	public void setInvestorType(String investorType) {
		this.investorType = investorType;
	}
	

}
